# ── Main ───────────────────────────────────────────────────────────────────

async def main() -> None:
    start_total = time.time()
    STEPS_DIR.mkdir(exist_ok=True)

    print("=" * 70, flush=True)
    print("GENESIS E2E PIPELINE — DIRECT claude -p", flush=True)
    print(f"Source: {SAMPLE_PROJECT}", flush=True)
    print(f"Output: {DOMAIN_OUTPUT}", flush=True)
    print(f"Steps:  {STEPS_DIR}", flush=True)
    print("=" * 70, flush=True)

    results: dict[str, str] = {}

    def trunc(s: str, n: int = 6000) -> str:
        return s[:n] if len(s) > n else s

    # ── Step 1: SourceScanner ──────────────────────────────────────────────
    print("\n--- STEP 1: SourceScanner ---", flush=True)
    t = time.time()
    r1 = await run_claude(build_scanner_prompt(), "SourceScanner", timeout=600)
    results["scan"] = r1
    (STEPS_DIR / "step1_scan.txt").write_text(r1)
    print_step_summary(1, "SourceScanner", r1, time.time() - t)

    if r1.startswith("Error:"):
        print("  [RETRY] Step 1 failed, retrying once...", flush=True)
        r1 = await run_claude(build_scanner_prompt(), "SourceScanner", timeout=600)
        results["scan"] = r1
        (STEPS_DIR / "step1_scan_retry.txt").write_text(r1)

    # ── Step 2: KnowledgeMapper ────────────────────────────────────────────
    print("\n--- STEP 2: KnowledgeMapper ---", flush=True)
    t = time.time()
    r2 = await run_claude(build_mapper_prompt(trunc(r1)), "KnowledgeMapper", timeout=600)
    results["map"] = r2
    (STEPS_DIR / "step2_map.txt").write_text(r2)
    print_step_summary(2, "KnowledgeMapper", r2, time.time() - t)

    if r2.startswith("Error:"):
        print("  [RETRY] Step 2 failed, retrying once...", flush=True)
        r2 = await run_claude(build_mapper_prompt(trunc(r1)), "KnowledgeMapper", timeout=600)
        results["map"] = r2
        (STEPS_DIR / "step2_map_retry.txt").write_text(r2)

    # ── Step 3: ContextEngineer ────────────────────────────────────────────
    print("\n--- STEP 3: ContextEngineer ---", flush=True)
    t = time.time()
    r3 = await run_claude(
        build_context_prompt(trunc(r1, 3000), trunc(r2, 3000)),
        "ContextEngineer",
        timeout=600,
    )
    results["ctx"] = r3
    (STEPS_DIR / "step3_ctx.txt").write_text(r3)
    print_step_summary(3, "ContextEngineer", r3, time.time() - t)

    if r3.startswith("Error:"):
        print("  [RETRY] Step 3 failed, retrying once...", flush=True)
        r3 = await run_claude(
            build_context_prompt(trunc(r1, 3000), trunc(r2, 3000)),
            "ContextEngineer",
            timeout=600,
        )
        results["ctx"] = r3
        (STEPS_DIR / "step3_ctx_retry.txt").write_text(r3)

    # ── Step 4: AgentArchitect ─────────────────────────────────────────────
    print("\n--- STEP 4: AgentArchitect ---", flush=True)
    t = time.time()
    r4 = await run_claude(
        build_architect_prompt(trunc(r1, 2000), trunc(r2, 2000), trunc(r3, 2000)),
        "AgentArchitect",
        timeout=600,
    )
    results["arch"] = r4
    (STEPS_DIR / "step4_arch.txt").write_text(r4)
    print_step_summary(4, "AgentArchitect", r4, time.time() - t)

    if r4.startswith("Error:"):
        print("  [RETRY] Step 4 failed, retrying once...", flush=True)
        r4 = await run_claude(
            build_architect_prompt(trunc(r1, 2000), trunc(r2, 2000), trunc(r3, 2000)),
            "AgentArchitect",
            timeout=600,
        )
        results["arch"] = r4
        (STEPS_DIR / "step4_arch_retry.txt").write_text(r4)

    # ── Step 5: AgentBuilder ───────────────────────────────────────────────
    print("\n--- STEP 5: AgentBuilder (writes files to disk) ---", flush=True)
    t = time.time()
    r5 = await run_claude(
        build_builder_prompt(
            trunc(r1, 2000),
            trunc(r2, 2000),
            trunc(r3, 5000),
            trunc(r4, 4000),
        ),
        "AgentBuilder",
        timeout=900,
        max_turns=30,
    )
    results["build"] = r5
    (STEPS_DIR / "step5_build.txt").write_text(r5)
    print_step_summary(5, "AgentBuilder", r5, time.time() - t)

    if r5.startswith("Error:"):
        print("  [RETRY] Step 5 failed, retrying once...", flush=True)
        r5 = await run_claude(
            build_builder_prompt(
                trunc(r1, 2000),
                trunc(r2, 2000),
                trunc(r3, 5000),
                trunc(r4, 4000),
            ),
            "AgentBuilder",
            timeout=900,
            max_turns=30,
        )
        results["build"] = r5
        (STEPS_DIR / "step5_build_retry.txt").write_text(r5)

    # ── After Step 5: check if files were actually written ─────────────────
    domain_yamls = list(DOMAIN_OUTPUT.rglob("*.yaml")) if DOMAIN_OUTPUT.exists() else []
    if not domain_yamls:
        print(
            "\n  [Builder] claude didn't write files directly — extracting from output...",
            flush=True,
        )
        written = extract_and_write_files(results.get("build", ""), DOMAIN_OUTPUT)
        if not written:
            print(
                "  [Builder] Extraction found no file blocks — also trying context step output...",
                flush=True,
            )
            written += extract_and_write_files(results.get("ctx", ""), DOMAIN_OUTPUT)

        if not written:
            print(
                "  [Builder] No files extracted — writing minimal domain structure from step data...",
                flush=True,
            )
            write_minimal_domain(
                architect_output=results.get("arch", ""),
                context_output=results.get("ctx", ""),
                scan_output=results.get("scan", ""),
                base_dir=DOMAIN_OUTPUT,
            )
        else:
            # Even with some files extracted, ensure domain.yaml exists
            if not (DOMAIN_OUTPUT / "domain.yaml").exists():
                write_minimal_domain(
                    architect_output=results.get("arch", ""),
                    context_output=results.get("ctx", ""),
                    scan_output=results.get("scan", ""),
                    base_dir=DOMAIN_OUTPUT,
                )

    # ── Step 6: QualityGate ────────────────────────────────────────────────
    print("\n--- STEP 6: QualityGate ---", flush=True)
    t = time.time()
    r6 = await run_claude(build_quality_prompt(), "QualityGate", timeout=300, max_turns=10)
    results["quality"] = r6
    (STEPS_DIR / "step6_quality.txt").write_text(r6)
    print_step_summary(6, "QualityGate", r6, time.time() - t)

    # ── Final summary ──────────────────────────────────────────────────────
    total_time = time.time() - start_total

    print(f"\n{'=' * 70}", flush=True)
    print("COMPLETE", flush=True)
    print(f"Duration: {total_time:.1f}s", flush=True)
    print(f"Steps dir: {STEPS_DIR}", flush=True)

    if DOMAIN_OUTPUT.exists():
        files = [f for f in DOMAIN_OUTPUT.rglob("*") if f.is_file()]
        print(f"Domain files: {len(files)}", flush=True)
        for f in sorted(files):
            size = f.stat().st_size
            print(f"  {f.relative_to(DOMAIN_OUTPUT)}  ({size} bytes)", flush=True)
    else:
        print("Domain output dir not created.", flush=True)

    # Print quality score if parseable
    try:
        import json
        q = json.loads(r6)
        print(f"\nQuality score: {q.get('score', 'N/A')}/100", flush=True)
        if q.get("issues"):
            print("Issues:", flush=True)
            for issue in q["issues"]:
                print(f"  - {issue}", flush=True)
        if q.get("missing_required"):
            print("Missing files:", flush=True)
            for f in q["missing_required"]:
                print(f"  - {f}", flush=True)
    except Exception:
        pass

    print(f"{'=' * 70}", flush=True)


if __name__ == "__main__":
    asyncio.run(main())
