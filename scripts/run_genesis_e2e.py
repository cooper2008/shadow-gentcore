# ── Core helper ────────────────────────────────────────────────────────────

async def run_claude(prompt: str, name: str, timeout: int = 900, max_turns: int = 30) -> str:
    """Run claude -p and return output. Prints real-time progress."""
    print(f"  [{name}] Sending to claude-p ({len(prompt)} chars)...", flush=True)

    cmd = ["claude", "-p", "--max-turns", str(max_turns), "--dangerously-skip-permissions"]
    env = {k: v for k, v in os.environ.items() if k != "ANTHROPIC_API_KEY"}

    if len(prompt) > 40000:
        prompt = prompt[:40000] + "\n\n[Context truncated. Produce your final output now.]"

    try:
        proc = await asyncio.create_subprocess_exec(
            *cmd,
            stdin=asyncio.subprocess.PIPE,
            stdout=asyncio.subprocess.PIPE,
            stderr=asyncio.subprocess.PIPE,
            env=env,
        )
        start = time.time()
        stdout, stderr = await asyncio.wait_for(
            proc.communicate(input=prompt.encode("utf-8")), timeout=timeout
        )
        elapsed = time.time() - start
        out = stdout.decode("utf-8", errors="replace").strip()
        if proc.returncode != 0 and not out:
            err = stderr.decode("utf-8", errors="replace").strip()
            if err:
                out = f"Error: {err}"
        print(f"  [{name}] Done in {elapsed:.1f}s ({len(out)} chars)", flush=True)
        return out
    except asyncio.TimeoutError:
        print(f"  [{name}] TIMEOUT after {timeout}s", flush=True)
        return f"Error: Timeout after {timeout}s"
    except Exception as e:
        print(f"  [{name}] FAILED: {e}", flush=True)
        return f"Error: {e}"


# ── Step summary printer ───────────────────────────────────────────────────

def print_step_summary(num: int, name: str, output: str, elapsed: float) -> None:
    status = "ERROR" if output.startswith("Error:") else "OK"
    preview = output[:200].replace("\n", " ")
    print(f"  [{name}] Step {num} {status} in {elapsed:.1f}s — preview: {preview!r}", flush=True)


# ── File extraction fallback ───────────────────────────────────────────────

def extract_and_write_files(content: str, base_dir: Path) -> list[str]:
    """Parse agent output and write any embedded file contents to disk."""
    written: list[str] = []

    # Pattern 1: ### FILE: path/to/file\n```lang\ncontent\n```
    pattern1 = re.compile(r"###\s+FILE:\s+([^\n]+)\n```[^\n]*\n(.*?)\n```", re.DOTALL)
    # Pattern 2: ## path/to/file (when path contains / or extension)
    pattern2 = re.compile(
        r"##\s+([\w/.-]+\.(?:yaml|md|json|py|txt))\n```[^\n]*\n(.*?)\n```", re.DOTALL
    )
    # Pattern 3: **path/to/file** code block
    pattern3 = re.compile(
        r"\*\*([\w/.-]+\.(?:yaml|md|json|py|txt))\*\*\s*\n```[^\n]*\n(.*?)\n```", re.DOTALL
    )

    for pattern in [pattern1, pattern2, pattern3]:
        for m in pattern.finditer(content):
            rel_path = m.group(1).strip()
            file_content = m.group(2)
            full_path = base_dir / rel_path
            full_path.parent.mkdir(parents=True, exist_ok=True)
            full_path.write_text(file_content)
            written.append(str(rel_path))
            print(f"  [Extracted] {rel_path}", flush=True)

    return written


