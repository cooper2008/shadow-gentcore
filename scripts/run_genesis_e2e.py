def _extract_standards(context_output: str, scan_output: str) -> str:
    """Extract standards content from context engineer or scan output."""
    # Try to find a standards section in context output
    for source in [context_output, scan_output]:
        if not source:
            continue
        # Look for a standards block
        m = re.search(
            r"(?:standards\.md|# Standards|## Standards)(.*?)(?:###|## [A-Z]|\Z)",
            source,
            re.DOTALL | re.IGNORECASE,
        )
        if m:
            extracted = m.group(1).strip()
            if len(extracted) > 100:
                return f"# Project Standards\n\n{extracted[:4000]}"

    # Generic fallback
    return textwrap.dedent("""\
        # Project Standards

        ## Code Quality
        - All code must pass linting before merge
        - Functions must have docstrings/JSDoc
        - Test coverage must exceed 80%

        ## Git Workflow
        - Feature branches from main
        - Pull requests required for all changes
        - Squash merge to main

        ## API Design
        - RESTful conventions
        - Versioned endpoints (/api/v1/...)
        - JSON request/response bodies
        - Standard error format: {error, message, code}

        ## Backend
        - Input validation on all endpoints
        - Structured logging
        - Environment-based configuration

        ## Frontend
        - TypeScript strict mode
        - Component-level tests
        - Accessible markup (WCAG 2.1 AA)
    """)


