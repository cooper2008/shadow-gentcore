#!/usr/bin/env python3
"""End-to-end genesis pipeline — direct claude -p bypass.

Bypasses harness/AgentRunner entirely. Each step calls `claude -p` directly
via asyncio subprocess, so native Write/Bash tools work inside claude and
file writes actually land on disk.

Usage:
    .venv/bin/python scripts/run_genesis_e2e.py
"""

